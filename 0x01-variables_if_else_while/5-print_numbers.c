#include<stdio.h>
/**
 * main -> prints all single digit numbers of base 10 starting from 0,
 * followed by a new line
 * Return: always 0 on success
 */
int main(void)
{
	int x;
	/**let number to be print take variable x */
	for (x = 0; x < 10; x++)
	{
	printf("%d", x);
	}
printf("\n");
return (0);
}
